public class master_class{
  int roll;
  int cgpa;
  String name;

  master_class(int r,int cgpa, String name){
  this.roll = r;
  this.cgpa = cgpa;
  this.name = name;
  }
  public int getRoll(){
    return roll;
  }
  public void setRoll(int roll){
    this.roll = roll;
  }
  public int getCgpa(){
    return cgpa;
  }
  public void setCgpa(int cgpa){
    this.cgpa = cgpa;
  }
  public String getName(){
    return name;
  }
  public void setCgpa(String name){
    this.name = name;
  }
}

